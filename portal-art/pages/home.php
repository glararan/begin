	<div class="title"><h4>Co je to Portal Art?</h4></div>
	
	<p><b>Portal Art</b> je webový projekt Střední školy aplikované kybernetiky s.r.o, který byl vytvořen během roku 2012/2013 na projektu označeným číslem 21. vytvořen. Portal Art má několik funkcí.</p><ul><li>Vypůjčení techniky</li><li>Uložiště zdrojových kodů pro programátory</li><li>A další...</li></ul><p>Portal Art byl vytvořený za účelem zrychlit půjčování techniky a rychlému šíření obsahu co studenti Střední školy vytvořili. Přihlašování je propojeno s databází emailů, takže se žádný student nemusí znovu registrovat. Pokuď bude student nahrávat nevhodný obsah může mu být vstup na Portal Art zakázan.<br><br>Tým Portal Art projektu byl ze dvou ročníku: T4 a T1. Vedoucí projektu byl Matěj Pokorný z T4.</p>

                <p>ROLLPA je název nábytkové stavebnice, který vznikl složením částí dvou anglických slov <b>ROLL</b>ed <b>PA</b>per, 
což v češtině znamená „rolovaný papír“. Právě „chytrým“ rolováním a lepením pásků z recyklovaného 
papíru totiž vznikají velmi lehké a neuvěřitelně pevné trubičky, které se v roce 1991 staly základem stejno-
jmenné nábytkové stavebnice, za jejíž řešení byl zakladatel a duchovní otec tohoto projektu PhDr.Václav 
Husák o rok později odměněn Design centrem České republiky cenou „Vynikající design 1992“.</p>
				<br>
				<p>ROLLPA je konstrukční systém založený na velmi jednoduchém principu. Trubky několika modulových 
délek vyrobené z papíru nebo plastu jsou s pomocí různých typů konektorů spojovány do prostorových 
příhradových konstrukcí vytvářených přesně na míru dle potřeb uživatele. Ty jsou pak po doplnění pevnými 
policemi a různými kartónovými doplňky, jako jsou potištěné skládací krabice a kontejnery se zásuvkami, 
široce využitelné ve všech oblastech lidského života.</p>
				<br>
				<p>ROLLPA  se stala důležitým komponentem další univerzální stavebnice s názvem Rocon, která úspěšně 
zkombinovala plastové trubky  Rollpa osazené speciálními koncovkami s klasickými nábytkářskými 
deskami. Výsledkem je  jedinečný konstrukční systém umožňující navrhovat nekonečné množství 
nábytkářských výrobků využitelných ve všech typech interiérů a při použití vodovzdorných desek i 
v exteriéru.</p>
